# ---------- VLM 剪辑专项任务 (Editing-focused) ----------
# 5 个任务覆盖剪辑分析的核心维度, 均要求模型输出可验证的时间戳, 便于后续截帧人工核对
VLM_EDITING_TASKS: dict[str, str] = {
    # 1. Hook = 视频开篇 3-10 秒内用于抓住观众的核心钩子
    "hook_identification": (
        "请从「视频剪辑」角度识别本视频的 Hook (开场吸引钩子)。"
        "Hook 通常出现在视频开头 0-10 秒, 通过强视觉冲击、悬念、反差、主题字幕等手段抓住观众。"
        "只输出 1 个最主要的 Hook, 严格按以下格式: \n"
        "[开始秒-结束秒] | Hook 类型 (视觉冲击/悬念/字幕标题/反差/其他) | 为什么有效 (一句话)"
    ),

    # 2. 分镜 = 由 cut 切分的最小镜头单元
    "shot_segmentation": (
        "请按时间顺序识别本视频中的关键分镜 (shot), 以画面 cut/转场为分界。"
        "控制在 8-12 个最具代表性的分镜以内, 每行格式严格为: \n"
        "[开始秒-结束秒] | 景别 (特写/近景/中景/全景/远景) | 镜头主体与动作描述"
    ),

    # 3. 专场 = 同一种功能/形式的连续片段 (字幕板专场、对白专场、动作专场等)
    "scene_typing": (
        "请识别本视频中的不同「专场」类型, 例如: "
        "字幕板专场 (纯文字提示画面) / 对白专场 (人物说话画面) / 动作专场 (打斗或快动作) / "
        "空镜专场 (无人物的环境镜头) / 信息板专场 (标题或数据展示) 等。"
        "按时间顺序输出 5-10 条, 每行格式严格为: \n"
        "[开始秒-结束秒] | 专场类型 | 该段内容描述"
    ),

    # 4. 卡点 = 镜头切换或节奏强变化的关键时间点
    "rhythm_cutpoints": (
        "请识别本视频中最重要的 5-8 个剪辑「卡点」, 即镜头切换或节奏发生强变化的关键时间点。"
        "每行格式严格为: \n"
        "[时间戳秒] | 卡点类型 (硬切/转场/音乐重音/视觉冲击/字幕弹出/其他) | 该卡点的作用"
    ),

    # 5. 三段式 = 开篇 / 主体高潮 / 收尾 的整体结构
    "three_act_structure": (
        "请按经典剪辑「三段式」(开篇 Setup / 主体高潮 Climax / 收尾 Outro) 划分本视频结构, "
        "每段给出时间区间和该段的核心功能。严格按以下 3 行格式输出: \n"
        "[开始秒-结束秒] | 开篇 | 段落功能与亮点\n"
        "[开始秒-结束秒] | 主体高潮 | 段落功能与亮点\n"
        "[开始秒-结束秒] | 收尾 | 段落功能与亮点"
    ),
}


